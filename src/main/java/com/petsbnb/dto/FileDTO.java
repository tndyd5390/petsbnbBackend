package com.petsbnb.dto;

public class FileDTO {
	private String fileNo;
	private String fileOrgName;
	private String fileName;
	private String filePath;
	private String regUserNo;
	private String regDt;
	private String chgUserNo;
	private String chgDt;
	public String getFileNo() {
		return fileNo;
	}
	public void setFileNo(String fileNo) {
		this.fileNo = fileNo;
	}
	public String getFileOrgName() {
		return fileOrgName;
	}
	public void setFileOrgName(String fileOrgName) {
		this.fileOrgName = fileOrgName;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getFilePath() {
		return filePath;
	}
	public void setFilePath(String filePath) {
		this.filePath = filePath;
	}
	public String getRegUserNo() {
		return regUserNo;
	}
	public void setRegUserNo(String regUserNo) {
		this.regUserNo = regUserNo;
	}
	public String getRegDt() {
		return regDt;
	}
	public void setRegDt(String regDt) {
		this.regDt = regDt;
	}
	public String getChgUserNo() {
		return chgUserNo;
	}
	public void setChgUserNo(String chgUserNo) {
		this.chgUserNo = chgUserNo;
	}
	public String getChgDt() {
		return chgDt;
	}
	public void setChgDt(String chgDt) {
		this.chgDt = chgDt;
	}
}
