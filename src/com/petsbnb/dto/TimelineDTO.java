package com.petsbnb.dto;

public class TimelineDTO {
	private String timelineInfoNo;
	private String reservationInfoNo;
	private String content;
	private String regDt;
	private String regNo;
	private String chgNo;
	private String chgdt;
	public String getTimelineInfoNo() {
		return timelineInfoNo;
	}
	public void setTimelineInfoNo(String timelineInfoNo) {
		this.timelineInfoNo = timelineInfoNo;
	}
	public String getReservationInfoNo() {
		return reservationInfoNo;
	}
	public void setReservationInfoNo(String reservationInfoNo) {
		this.reservationInfoNo = reservationInfoNo;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getRegDt() {
		return regDt;
	}
	public void setRegDt(String regDt) {
		this.regDt = regDt;
	}
	public String getRegNo() {
		return regNo;
	}
	public void setRegNo(String regNo) {
		this.regNo = regNo;
	}
	public String getChgNo() {
		return chgNo;
	}
	public void setChgNo(String chgNo) {
		this.chgNo = chgNo;
	}
	public String getChgdt() {
		return chgdt;
	}
	public void setChgdt(String chgdt) {
		this.chgdt = chgdt;
	}
}
